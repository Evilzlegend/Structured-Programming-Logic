# TOPIC SUMMARY:
# A variable is a name for a piece of data. To create a variable in a Python program, we use an
# assignment statement, which creates the variable (if it doesn't already exist) and assigns it to hold a
# given value.

# CODING EXAMPLE:
# The general form of an assignment statemtns is:

# <variable> = <value>

# The variable must appear to the left of the equal sign, and its value appears to the right. The value may
# be either a simple piece of data, or an expression that evaluates to some data value.

