# TOPIC SUMMARY:
# A comment is text that is placed into a program for human use, and (mostly) ignored by the computer.
# A comment is meant to explain an interesting or cryptic part of the code, to help us remember what
# the code is doing, and to make it easier for others to read and understand our code.

# Single-line comments are generally used to:
# (1) introduce some lines of code that perform some identificable task,
# (2) to annotate lines of interest,
# (3) to explain the meaning or use of a variable, or
# (4) to explain a line whose purpose may not be clear from its code.

# TOPIC EXPLANATION:
# In Python, a single-line comment may occur on its own line: a block comment. Or it may occur at the
# end of an existing line of code: an inline comment. It is introduced by the special character "#" and runs
# to the end of that line. We often "comment out" a line of code with a block comment when debugging
# or when a program is incomplete, to prevent the code from being executed.

