# TOPIC SUMMARY:
# The Boolean data type is used for logical statements. The constants that belong to the type are the
# values. "True" and "False". With expressions that evaluate to Boolean values, programs can ask
# questions and make decisions based on the results.

# TOPIC EXPLANATION:
# One of the most common kinds of Boolean expressions are comparisons, where we ask if two values
# are equal or not equal, less than or greater than. The six main comparison operators are: equal "==",
# not-equal "!=", less-than "<", less than or equal to "<=", greater-thank ">", and greater than or equal to
# ">=".

# These operators are easy to interpret for numbers. When applied to strings, they perform a kind of
# alphabetical ordering, based on ASCII/Unicode encodings of characters.

