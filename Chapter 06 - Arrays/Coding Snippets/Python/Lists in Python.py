# TOPIC SUMMARY:
# A list is a collection of data that is organized in a linear order. Each element of a
# list has a position in the list. Lists are written with square brackets "[" and "]"
# around the data they contain. You can also create a list with the "list" function.

# TOPIC EXPLANATION:
# The length of a list is the number of elements in it. Lists may have any number of
# elements, including zero. The empty list is a very special list containing zero
# elements.

# The data in a list may be of any type, and we can mix together different kinds of
# data in the same list. We can even build lists that have lists inside of them (called
# sublists).

