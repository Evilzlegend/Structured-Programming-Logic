# TOPIC SUMMARY:
# Debugging is one of the most important skills a programmer can develop.

# TOPIC EXPLANATION:
# When faced with an error, try the following debugging methods:

# 1. Read the error message carefully, find the relevant code, and work through the code by hand, on
# on paper, seeking what went wrong.

# 2. Add print statements before the error to see what code is executing and the values of relevant
# variables.

# 3. Comment out all the relevant code, then put the pieces back one by one, printing the results, until
# you pinpoint where the error is created.

# 4. If you program with a Python IDE, it may come with a debugger: learn to use it to step through your
# code.

# Ask a friend to look at your code, and walk through it with them, explaining it line by line.

# PLAYING WITH CODING SNIPPETS:
# 1. Hit the Run button to see the output in the snippet box.
# 2. Play around with the snippet code - change variable names, parameters, etc.
# 3. Hit the Run button again to see what works and what does not work after you manipulate the code.
# 4. Run the correct code again to compare it to the manipulated code.
# 5. Have fun practicing this snippet as many times as you like - there are no limits!

