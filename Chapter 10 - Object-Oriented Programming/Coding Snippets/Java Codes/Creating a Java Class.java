/*
Topic Summary
All Java application classes have several features in common. They all have a header that begins with the keywordspublic class, followed by the name of the class. They all contain a pair of curly braces that surround the class body. Between the curly braces, all applications contain a main() method whose standard header is public static void main(String[] args). The main() method has its own set of curly braces between which you write statements that the method will execute. Each complete statement within a method ends in a semicolon.
*/


public class Greeting
{
    public static void main(String[] args)
   {
        System.out.println("Hello there!");
        System.out.println("Good to see you!");
   }
}